# Creating code with Python
# Printing 1st code
print("Hello, World!")

# Improving the 1st Python program
input("What's your name? ")
print("hello, World!")

# Defining a variable name and printing "hello" with the variable
name = input("What's your name? ")
print("Hello,")
print(name)


# Comments
# Ask the user for their name
name = input("What's your name? ")
print("hello,")
print(name)


# Pseudocode
# Ask the user for their name
name = input("What's your name? ")

# Print Hello
print("Hello,")

# Print the name inputted
print(name)


# Further improving the 1st Python program
# Ask the user for their name
name = input("What's your name? ")

# Print hello and the inputted name
print("Hello,", name)

# Strings and Paremeters
# Ask the user for their name
name = input("What's your name? ")
print("hello,")
print(name)

# Ask the user for their name
name = input("What's your name? ")
print("Hello,", end="")
print(name)

# Formatting Strings
# Ask the user for their name
name = input("What's your name? ")
print(f"Hello, {name}!")